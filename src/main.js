function showBookInsight(key) {
  const detail = t().insights[key] || t().insights[defaultBookKey]
  if (!detail) {
    return
  }

  activeInsightKey = key in t().insights ? key : defaultBookKey

  bookImage.src = detail.image
  bookImage.alt = `${t().ui.bookImageAltPrefix}: ${detail.title}`
  bookTerm.textContent = detail.title
  bookInfo.textContent = detail.info
  bookQuote.textContent = detail.quote

  if (detail.source) {
    const sourceLabel = t().ui.sourceLabel || 'Source'
    bookSource.hidden = false
    bookSource.textContent = `${sourceLabel}: ${detail.source}`
  } else {
    bookSource.hidden = true
    bookSource.textContent = ''
  }
}

function paragraphBlock(pageText) {
  return `<p>${annotateText(pageText)}</p>`
}

function renderSpread() {
  const left = pages[spreadStart] ?? ''
  const right = pages[spreadStart + 1] ?? ''

  leftPageContent.innerHTML = paragraphBlock(left)
  rightPageContent.innerHTML = paragraphBlock(right)

  leftPageNumber.textContent = `p. ${spreadStart + 1}`
  rightPageNumber.textContent = `p. ${Math.min(spreadStart + 2, pages.length)}`
  spreadIndicator.textContent = `${t().ui.pagesLabel} ${spreadStart + 1}-${Math.min(spreadStart + 2, pages.length)} / ${pages.length}`

  prevPageButton.disabled = spreadStart === 0
  nextPageButton.disabled = spreadStart + 2 >= pages.length
}

function turnPage(direction) {
  if (turning) {
    return
  }

  const canTurnForward = direction === 'next' && spreadStart + 2 < pages.length
  const canTurnBackward = direction === 'prev' && spreadStart > 0
  if (!canTurnForward && !canTurnBackward) {
    return
  }

  turning = true
  bookShell.classList.remove('turn-next', 'turn-prev')
  void bookShell.offsetWidth
  bookShell.classList.add(direction === 'next' ? 'turn-next' : 'turn-prev')

  window.setTimeout(() => {
    spreadStart += direction === 'next' ? 2 : -2
    renderSpread()
  }, 170)

  window.setTimeout(() => {
    bookShell.classList.remove('turn-next', 'turn-prev')
    turning = false
  }, 360)
}

