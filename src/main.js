function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

let quizState = { shuffled: [], answered: 0, correct: 0 }

const backgroundSong = new Audio('/song.mp3')
backgroundSong.preload = 'auto'
backgroundSong.loop = true
backgroundSong.volume = 0.55

function syncMusicState() {
  isSongEnabled = !backgroundSong.paused
  musicToggle.textContent = isSongEnabled ? t().ui.musicOff : t().ui.musicOn
  musicToggle.setAttribute('aria-pressed', String(isSongEnabled))
}

function toggleSong() {
  if (backgroundSong.paused) {
    backgroundSong.play().catch(() => {
      syncMusicState()
    })
    return
  }

  backgroundSong.pause()
}

function handleQuizAnswer(btn) {
  const qi = Number(btn.dataset.qi)
  const questionEl = document.querySelector(`#quiz-q-${qi}`)
  const btns = questionEl.querySelectorAll('.quiz-btn')
  const resultEl = document.querySelector(`#quiz-result-${qi}`)
  const q = quizState.shuffled[qi]

  btns.forEach((b) => {
    b.disabled = true
    if (b.dataset.correct === 'true') b.classList.add('correct')
  })

  if (btn.dataset.correct === 'true') {
    quizState.correct++
    resultEl.textContent = q.feedback.correct
    resultEl.className = 'quiz-result result-ok'
  } else {
    btn.classList.add('wrong')
    resultEl.textContent = q.feedback.wrong
    resultEl.className = 'quiz-result result-bad'
  }

  quizState.answered++
  document.querySelector('#quiz-score').textContent =
    `${t().ui.scoreLabel}: ${quizState.correct} / ${quizState.shuffled.length}`

  if (quizState.answered === quizState.shuffled.length) {
    document.querySelector('#quiz-end').style.display = ''
  }
}

function renderQuiz() {
  const container = document.querySelector('#quiz-container')
  const total = quizState.shuffled.length
  container.innerHTML = `
    <div class="quiz-header">
      <h2>${t().ui.quizTitle}</h2>
      <span class="quiz-score" id="quiz-score">${t().ui.scoreLabel}: 0 / ${total}</span>
    </div>
    ${quizState.shuffled.map((q, i) => `
      <div class="quiz-question" id="quiz-q-${i}">
        <p class="question-text">${i + 1}. ${q.question}</p>
        <div class="choice-row">
          ${q.answers.map((a, ai) => `
            <button class="quiz-btn" data-qi="${i}" data-ai="${ai}" data-correct="${a.correct}" type="button">
              ${String.fromCharCode(65 + ai)}. ${a.label}
            </button>
          `).join('')}
        </div>
        <p class="quiz-result" id="quiz-result-${i}"></p>
      </div>
    `).join('')}
    <div id="quiz-end" style="display:none">
      <button class="quiz-retry-btn" type="button">${t().ui.retryQuiz}</button>
    </div>
  `
  container.querySelectorAll('.quiz-btn').forEach((btn) => {
    btn.addEventListener('click', () => handleQuizAnswer(btn))
  })
  document.querySelector('.quiz-retry-btn').addEventListener('click', initQuiz)
}

function initQuiz() {
  quizState = { shuffled: shuffle(t().quizQuestions), answered: 0, correct: 0 }
  renderQuiz()
}

