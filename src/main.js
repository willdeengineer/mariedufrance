function escapeHtml(text) {
  return text
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
}

function escapeRegExp(text) {
  return text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function annotateText(paragraph) {
  let html = escapeHtml(paragraph)
  const tags = t().tags || []

  tags.forEach((tag) => {
    const pattern = new RegExp(`\\b${escapeRegExp(tag.label)}\\b`, 'gi')
    html = html.replace(pattern, (match) => {
      return `<button class="inline-hotspot" data-term="${tag.key}" type="button">${match}</button>`
    })
  })

  return html
}

function toPages(raw) {
  const normalized = raw.replaceAll('\n', ' ').replace(/\s+/g, ' ').trim()
  const sentences = normalized.split(/(?<=[.!?])\s+/).filter(Boolean)
  const maxCharsPerPage = 1100

  const pages = []
  let buffer = ''

  sentences.forEach((sentence) => {
    const next = buffer ? `${buffer} ${sentence}` : sentence

    if (next.length > maxCharsPerPage && buffer) {
      pages.push(buffer)
      buffer = sentence
      return
    }

    buffer = next
  })

  if (buffer) {
    pages.push(buffer)
  }

  return pages
}

