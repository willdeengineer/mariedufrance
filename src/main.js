function buildHeroTitleHtml() {
  const ui = t().ui
  const escapeForRegex = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const laiPattern = new RegExp(escapeForRegex(ui.heroLinkLai), 'i')
  const mariePattern = new RegExp(escapeForRegex(ui.heroLinkMarie), 'i')

  let titleHtml = escapeHtml(ui.heroTitle)

  titleHtml = titleHtml.replace(laiPattern, (match) => {
    return `<button class="title-link" data-term="lai" type="button">${match}</button>`
  })

  titleHtml = titleHtml.replace(mariePattern, (match) => {
    return `<button class="title-link" data-term="marie" type="button">${match}</button>`
  })

  if (!titleHtml.includes('data-term="lai"') || !titleHtml.includes('data-term="marie"')) {
    titleHtml += ` <button class="title-link" data-term="lai" type="button">${escapeHtml(ui.heroLinkLai)}</button> <button class="title-link" data-term="marie" type="button">${escapeHtml(ui.heroLinkMarie)}</button>`
  }

  return titleHtml
}

